def render_guide_page() -> None:
    """評価ルール詳細ページ."""
    st.title("📘 評価ルール詳細")
    st.caption(
        "全評価項目の「法令根拠 / 何をチェック / 評価ロジック / 実務メモ / 連絡先 / 重み」を網羅。"
        "重みを変えると評価結果のスコアが連動して変化します（セッション中保持）。"
    )

    # Session state 初期化
    if "weights" not in st.session_state:
        st.session_state.weights = DEFAULT_WEIGHTS.copy()

    # コントロール
    c1, c2, c3 = st.columns([1, 1, 4])
    with c1:
        if st.button("🔄 重みをデフォルトに戻す"):
            st.session_state.weights = DEFAULT_WEIGHTS.copy()
            st.rerun()
    with c2:
        if st.button("🔵 全て5.0に均等化"):
            st.session_state.weights = {k: 5.0 for k in DEFAULT_WEIGHTS}
            st.rerun()
    with c3:
        total_w = sum(st.session_state.weights.values())
        st.info(f"📊 現在の重み合計: **{total_w:.1f}**")

    st.divider()

    # カテゴリ別にグループ化
    cats: Dict[str, List[Dict]] = {}
    for r in RULE_DETAILS:
        cats.setdefault(r["category"], []).append(r)

    # フィルタ
    selected_cat = st.selectbox(
        "カテゴリで絞り込み",
        options=["全て"] + list(cats.keys()),
        index=0,
    )

    for cat, rules in cats.items():
        if selected_cat != "全て" and selected_cat != cat:
            continue
        st.header(f"📂 {cat}")
        for rule in rules:
            _render_rule(rule)


def _render_rule(rule: Dict) -> None:
    """1つのルールの詳細表示."""
    with st.expander(f"📌 {rule['label']}", expanded=False):
        # 法令根拠
        st.markdown(f"**📜 法令根拠**：{rule['legal_basis']}")

        # 重みスライダー（該当する場合）
        w_key = rule.get("weight_key")
        if w_key and w_key in DEFAULT_WEIGHTS:
            current = st.session_state.weights.get(w_key, DEFAULT_WEIGHTS[w_key])
            new_w = st.slider(
                f"⚖️ 重みづけ（このルールを総合スコアでどれだけ重視するか）",
                min_value=0.0,
                max_value=10.0,
                value=float(current),
                step=0.5,
                key=f"slider_{rule['key']}",
            )
            st.session_state.weights[w_key] = new_w

        # 何をチェックしているか
        st.markdown("**🔍 何をチェックしているか**")
        for c in rule.get("checks", []):
            st.markdown(f"- {c}")

        # 評価ロジック
        st.markdown("**📐 評価ロジック（スコア計算）**")
        st.markdown(rule.get("scoring", "—"))

        # 実務メモ
        st.markdown("**💼 実務メモ（建築士・コンサルの現場感）**")
        st.markdown(rule.get("practical", "—"))

        # 対応方針
        st.markdown(f"**🎯 対応方針**：{rule.get('response', '—')}")

        # 連絡先
        st.markdown(f"**📞 連絡先・関係者**")
        st.markdown(rule.get("contact", "—"))
